export function getImpactSym(player) {
    
    
    switch(player) {
        
        case '🜃': 
            return '🌱'   
        case '🜄':
            return '💧'
        case '🜁':
            return '🌪'
        case '🜂':
            return '🔥'
        default:
            return null
    }
}

            